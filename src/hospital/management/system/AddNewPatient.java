    public void validFields() {
        String patientId = txtId.getText();
        String name = txtName.getText();
        String contact = txtContact.getText();
        String age = txtAge.getText();
        String gender = (String) jComboBox1.getSelectedItem();
        String bloodGroup = txtBloodGroup.getText();
        String address = txtAddress.getText();
        String Disease = txtMajorDisease.getText();
        if (!patientId.equals("") && !name.equals("") && !contact.equals("") && !age.equals("") && !gender.equals("") && !bloodGroup.equals("") && !address.equals("") && !Disease.equals("")) {
            btnSave.setEnabled(true);
        } else {
            btnSave.setEnabled(false);
        }
    }


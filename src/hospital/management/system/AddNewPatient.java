    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        int patientId = Integer.parseInt(txtId.getText());
        String name = txtName.getText();
        String contact = txtContact.getText();
        int age = Integer.parseInt(txtAge.getText());
        String gender = (String) jComboBox1.getSelectedItem();
        String bloodGroup = txtBloodGroup.getText();
        String address = txtAddress.getText();
        String Disease = txtMajorDisease.getText();
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            String query = "insert into patients values(" + patientId + ", ' " + name + " ',' " + contact + " ', " + age + ", ' " + gender + " ', ' " + bloodGroup + " ', ' " + address + " ', ' " + Disease + " ' )";

            st.executeUpdate(query);
            JOptionPane.showMessageDialog(null, "<html><span style=\"color:green;\">Successfully Updated</span></html>");
            clear();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "<html><span style=\"color:red;\">" + e + "</span></html>");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

